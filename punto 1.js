//scribe un programa que pida al usuario ingresar un número, luego pide un segundo número e imprime en la consola la suma de los dos números ingresados.
let numero1 = prompt("Ingrese el primer número:");

numero1 = numero1 - 0;

let numero2 = prompt("Ingrese el segundo número:");

numero2 = numero2 - 0;

let suma = numero1 + numero2;
console.log(`La suma de ${numero1} y ${numero2} es: ${suma}`);